//Switch Case

function teste(key) {
    switch (key) {
        case 'Cleber':
            console.log(`${key} aqui`)
            break;
        case 'Bento':
            console.log(`${key} aqui`)
        break;
        case 'Clara':
            console.log(`${key} aqui`)
        break;
        default:
            console.log('Outro aqui')
            break;
    }
}

teste('Cleber');